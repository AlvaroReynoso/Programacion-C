#include <stdio.h>
#include <string.h>

void main() {
    // Declarar un arreglo de caracteres para almacenar la palabra e imprimir la palabra ingresada
    char palabra[100];
    printf("Ingresa una palabra: ");
    scanf("%s", palabra); // Lee la palabra ingresada por el usuario y la guarda en el arreglo 'palabra'

    // Mostrar la palabra ingresada
    printf("La palabra ingresada es: %s\n", palabra);
    

}